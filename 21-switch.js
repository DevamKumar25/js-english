
// switch is use to avoid if-else condition

const month = 3

switch (month) {
    case 1:
        console.log("january")
        break;
        //  for copy we use shift+alt+downarrow
    case 2:
        console.log("feb")
        break;
    case 3:
        console.log("march")
        break;
    case 4:
        console.log("april")
        break;
    case 5:
        console.log("dec")
        break;
        


    default:
        console.log("default case match")
        break;
}